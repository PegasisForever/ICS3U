/*
 * Class to calculate perimeter of rectangles
 * Kevin JianQing Liu
 */
public class RectanglePerimeterKevin {
    public static void main(String[] args) {
        float width = 6;
        float height = 7.5f;
        float perimeter = width * 2 + height * 2;
        System.out.println("The perimeter of a " + width + "x" + height + " rectangle is " + perimeter);

        width = 7.2f;
        height = 8.5f;
        perimeter = width * 2 + height * 2;
        System.out.println("The perimeter of a " + width + "x" + height + " rectangle is " + perimeter);

        width = 6;
        height = 7.5f;
        perimeter = width * 2 + height * 2;
        System.out.println("The perimeter of a " + width + "x" + height + " rectangle is " + perimeter);
    }
}
